// TestGenerateBytes_ContainsExpectedPayload checks that the generated source
// contains the string literals from the input ontology, confirming the
// codegen actually wires the values rather than emitting empty stubs.
func TestGenerateBytes_ContainsExpectedPayload(t *testing.T) {
	ont := testOntology()

	src, err := scaffold.GenerateBytes(ont)
	require.NoError(t, err)

	s := string(src)

	// Prefix IRIs should appear in the output.
	assert.Contains(t, s, `"soc2"`, "prefix key 'soc2' should be in generated source")
	assert.Contains(t, s, `"https://trust.aicpa.org/soc2#"`, "prefix IRI for soc2 should appear")
	assert.Contains(t, s, `"nist-ai"`, "prefix key 'nist-ai' should appear")

	// Hierarchy entries.
	assert.Contains(t, s, `"soc2:CC6.1"`, "hierarchy label soc2:CC6.1 should appear")
	assert.Contains(t, s, `"soc2:CC6"`, "hierarchy subClassOf soc2:CC6 should appear")

	// Equivalence pair.
	assert.Contains(t, s, `"nist-ai:GOVERN-1.1"`, "equivalence IRI should appear")

	// IFP.
	assert.Contains(t, s, `"control_id"`, "IFP property should appear")

	// Package and function declarations.
	assert.Contains(t, s, "package gen", "generated file must declare package gen")
	assert.Contains(t, s, "func OntologyExtension()", "must export OntologyExtension func")
	assert.Contains(t, s, "graphrag.OntologyExtension{", "must return OntologyExtension struct literal")
}

// TestGenerateBytes_PrefixSortOrder verifies that the generated map literal
// has keys in lexicographic order, not Go runtime map order.
func TestGenerateBytes_PrefixSortOrder(t *testing.T) {
	ont := taxonomy.Ontology{
		Version: "1.0",
		Prefixes: map[string]string{
			"zzz": "https://z.example/",
			"aaa": "https://a.example/",
			"mmm": "https://m.example/",
		},
	}

	src, err := scaffold.GenerateBytes(ont)
	require.NoError(t, err)
	s := string(src)

	aPos := bytes.Index([]byte(s), []byte(`"aaa"`))
	mPos := bytes.Index([]byte(s), []byte(`"mmm"`))
	zPos := bytes.Index([]byte(s), []byte(`"zzz"`))

	require.Greater(t, aPos, -1, "key 'aaa' must appear in output")
	require.Greater(t, mPos, -1, "key 'mmm' must appear in output")
	require.Greater(t, zPos, -1, "key 'zzz' must appear in output")

	assert.Less(t, aPos, mPos, "key 'aaa' must come before 'mmm' in sorted output")
	assert.Less(t, mPos, zPos, "key 'mmm' must come before 'zzz' in sorted output")
}
