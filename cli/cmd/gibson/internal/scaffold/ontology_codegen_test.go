// testOntology returns a deterministic Ontology value used across tests.
func testOntology() taxonomy.Ontology {
	return taxonomy.Ontology{
		Version: "1.0",
		Prefixes: map[string]string{
			"soc2":    "https://trust.aicpa.org/soc2#",
			"nist-ai": "https://airc.nist.gov/rmf#",
		},
		Hierarchies: []taxonomy.HierarchyDef{
			{NodeType: "control", Label: "soc2:CC6", SubClassOf: ""},
			{NodeType: "control", Label: "soc2:CC6.1", SubClassOf: "soc2:CC6"},
			{NodeType: "control", Label: "soc2:CC6.2", SubClassOf: "soc2:CC6"},
		},
		Equivalences: [][2]string{
			{"soc2:CC6.1", "nist-ai:GOVERN-1.1"},
		},
		IFPs: []taxonomy.IFPDef{
			{NodeType: "control", Property: "control_id"},
		},
	}
}

// TestGenerateBytes_Deterministic runs GenerateBytes twice on the same input
// and asserts the outputs are byte-for-byte identical. This is the primary
// contract for codegen stability: version-control diffs only appear on
// intentional ontology changes.
func TestGenerateBytes_Deterministic(t *testing.T) {
	ont := testOntology()

	first, err := scaffold.GenerateBytes(ont)
	require.NoError(t, err)

	second, err := scaffold.GenerateBytes(ont)
	require.NoError(t, err)

	assert.True(t, bytes.Equal(first, second),
		"GenerateBytes must produce byte-identical output on repeated calls with the same input")
}

// TestGenerateBytes_MapKeyOrder verifies that map key ordering is stable
// across Go's random map iteration by using a prefix map with many keys.
func TestGenerateBytes_MapKeyOrder(t *testing.T) {
	ont := taxonomy.Ontology{
		Version: "1.0",
		Prefixes: map[string]string{
			"zebra":  "https://example.com/z#",
			"alpha":  "https://example.com/a#",
			"middle": "https://example.com/m#",
			"beta":   "https://example.com/b#",
		},
	}

	// Run many times to catch non-determinism from map iteration.
	first, err := scaffold.GenerateBytes(ont)
	require.NoError(t, err)

	for i := 0; i < 50; i++ {
		got, err := scaffold.GenerateBytes(ont)
		require.NoError(t, err)
		assert.True(t, bytes.Equal(first, got),
			"iteration %d: output differed; map keys must be sorted", i)
	}
}

// TestGenerateBytes_ValidGoSource parses the generated bytes with go/parser
// to confirm the output is syntactically valid Go. This catches template
// bugs that produce malformed source without requiring a full go build.
func TestGenerateBytes_ValidGoSource(t *testing.T) {
	ont := testOntology()

	src, err := scaffold.GenerateBytes(ont)
	require.NoError(t, err)
	require.NotEmpty(t, src)

	fset := token.NewFileSet()
	_, parseErr := parser.ParseFile(fset, "ontology_extension.go", src, parser.AllErrors)
	require.NoError(t, parseErr, "generated source must be valid Go:\n%s", src)
}

